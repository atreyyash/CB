#include<iostream>
using namespace std;

int main() {
    //Bitwise AND
    cout<<"5 & 7 : "<<(5&7)<<endl;
    cout<<"500 & 600 : "<<(500 & 600)<<endl;

    //Bitwise OR
    cout<<"5 | 7 : "<<(5 | 7)<<endl;
    cout<<"5 | 10 : "<<(5 | 10)<<endl;

    //Bitwise XOR
    cout<<"5 ^ 7 : "<<(5 ^ 7)<<endl;
    cout<<"5 ^ 10 : "<<(5 ^ 10)<<endl;
    

    return 0;
}